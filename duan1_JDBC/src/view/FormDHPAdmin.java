    public void loadMonHoc(List<MonHoc> list) {
        jTable2.setModel(tbmodel2);
        tbmodel2.setRowCount(0);
        String title[] = {"Tên Môn", "Học Phí", "Thời Lượng"};
        tbmodel2.setColumnIdentifiers(title);
        for (MonHoc mh : list) {
            DecimalFormat formatter = new DecimalFormat("###,###,###");
            String tl = String.valueOf(mh.getThoiLuong()) + " Tháng";
            tbmodel2.addRow(new Object[]{mh.getTenMon(), //hk,
                formatter.format(mh.getHocPhi()) + " VNĐ", tl

            });
        }
    }

