    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int row1 = jTable1.getSelectedRow();
        int row2 = jTable2.getSelectedRow();
        if (row1 >= 0 || row2 >= 0) {
            MonHoc mh = listMH.get(row2);
            HocVien hv = listHV.get(row1);
            DongHocPhi dhp = new DongHocPhi();
            dhp.setIdHocVien(hv.getIdHocVien());
            dhp.setIdMonHoc(mh.getId());
            dhp.setTienDong(mh.getHocPhi());
            for (DongHocPhi donghp : listDHP) {
                if (dhp.getIdHocVien().equalsIgnoreCase(donghp.getIdHocVien())
                        && dhp.getIdMonHoc().equalsIgnoreCase(donghp.getIdMonHoc())) {
                    JOptionPane.showMessageDialog(this, "Học Viên " + hv.getMaHV()
                            + " Đã Đóng Học Phí Môn " + mh.getTenMon());
                    return;
                }
            }
            if (admsv.addDHP(dhp)) {
                admsv.updateTTHV(hv.getIdHocVien());
                JOptionPane.showMessageDialog(this, "Tạo thành công!");
            } else {
                JOptionPane.showMessageDialog(this, "Tạo thất bại!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Chọn Học Viên Và Môn Học!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

